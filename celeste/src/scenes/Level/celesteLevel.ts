function fget(tile: number, flag: number) {
  return tile < mask.length && (mask[tile] & (1 << flag)) != 0;
}

class CelesteLevel {
  data: number[];
  level: number;

  constructor(level = 0) {
    this.data = mapData;
    this.level = level;
  }

  private getRoomCoords() {
    const roomY = Math.floor(this.level / 8);
    const roomX = this.level % 8;
    return { roomX, roomY };
  }

  getLevelObjects() {
    let playerSpawn = [0, 0];
    const springs: Array<[number, number]> = [];
    const fallFloors: Array<[number, number]> = [];
    const fakeWalls: Array<[number, number]> = [];
    const keys: Array<[number, number]> = [];
    const fruits: Array<[number, number]> = [];
    const flyFruits: Array<[number, number]> = [];
    const chests: Array<[number, number]> = [];
    const balloons: Array<[number, number]> = [];
    const platforms: Array<[number, number, number]> = [];
    const messages: Array<[number, number]> = [];
    const bigChests: Array<[number, number]> = [];

    const { roomX, roomY } = this.getRoomCoords();
    for (let tx = 0; tx <= 15; tx++) {
      for (let ty = 0; ty <= 15; ty++) {
        const x = tx * 8,
          y = ty * 8;
        const tile = this.data[roomX * 16 + tx + (roomY * 16 + ty) * 128];
        if (tile == 1) {
          playerSpawn = [x, -y];
        } else if (tile == 18) {
          springs.push([x, -y]);
        } else if (tile == 23) {
          fallFloors.push([x, -y]);
        } else if (tile == 8) {
          keys.push([x, -y]);
        } else if (tile == 26) {
          fruits.push([x, -y]);
        } else if (tile == 20) {
          chests.push([x, -y]);
        } else if (tile == 22) {
          balloons.push([x, -y]);
        } else if (tile == 11) {
          platforms.push([x, -y, -1]);
        } else if (tile == 12) {
          platforms.push([x, -y, 1]);
        } else if (tile == 28) {
          flyFruits.push([x, -y]);
        } else if (tile === 64) {
          fakeWalls.push([x, -y]);
        } else if (tile === 86) {
          messages.push([x, -y]);
        } else if (tile === 96) {
          bigChests.push([x, -y]);
        }
      }
    }

    // fix stuck
    if (this.level === 13) {
      playerSpawn[1] += 0.1;
    }

    return {
      playerSpawn,
      springs,
      fallFloors,
      fakeWalls,
      fruits,
      flyFruits,
      keys,
      chests,
      balloons,
      platforms,
      messages,
      bigChests,
    };
  }

  isTitle() {
    return this.level == 31;
  }
  /**
   *
   * @param mask 2 - background, 1 - mid, 3 - foreground
   * @returns
   */
  getLevelMap(mask = 0 | 1 | 2 | 3) {
    const mw = 16,
      mh = 16;

    let tx = 0;

    if (this.isTitle()) {
      tx = -4;
    }

    const clips: Array<[number, number, number, number]> = [];
    const tiles: Array<[number, number]> = [];
    const solids: Array<[number, number]> = [];
    const spikes: Array<[number, number, number]> = [];

    const { roomX, roomY } = this.getRoomCoords();

    const mx = roomX * 16;
    const my = roomY * 16;

    for (let x = 0; x < mw; x++) {
      for (let y = 0; y < mh; y++) {
        const tile = this.data[x + mx + (y + my) * 128];
        if (mask == 0 || fget(tile, mask)) {
          const clip = spr(tile);
          const t = [tx + x, -y] as [number, number];
          clips.push(clip);
          tiles.push(t);
          if (fget(tile, 0)) {
            solids.push(t);
          }
          // 1-top 2-bottom 3-left 4-right
          if (tile == 17) {
            spikes.push([t[0] * 8 + 1, t[1] * 8 - 3, 1]);
          } else if (tile === 27) {
            spikes.push([t[0] * 8, t[1] * 8, 2]);
          } else if (tile === 43) {
            spikes.push([t[0] * 8, t[1] * 8, 4]);
          } else if (tile === 59) {
            spikes.push([t[0] * 8, t[1] * 8, 3]);
          }
        }
      }
    }

    return {
      clips,
      tiles,
      solids,
      spikes,
    };
  }

  getLevelMapAll() {
    const bg = this.getLevelMap(2);
    const md = this.getLevelMap(1);
    const fg = this.getLevelMap(3);
    const clips = bg.clips.concat(md.clips).concat(fg.clips);
    const tiles = bg.tiles.concat(md.tiles).concat(fg.tiles);
    const solids = bg.solids.concat(md.solids).concat(fg.solids);
    const spikes = bg.spikes.concat(md.spikes).concat(fg.spikes);

    return {
      clips,
      tiles,
      solids,
      spikes,
    };
  }

  setLevel(level: number) {
    this.level = level;
  }
  // 2 1
  getLevelBGM() {
    if (this.isTitle()) {
      return "/sounds/title.mp3";
    }
    if (this.level >= 22) {
      return "/sounds/area3.mp3";
    } else if (this.level >= 21) {
      return "/sounds/wind.mp3";
    } else if (this.level >= 12) {
      return "/sounds/area2.mp3";
    } else if (this.level >= 11) {
      return "/sounds/wind.mp3";
    } else {
      return "/sounds/area1.mp3";
    }
  }
}

